void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putint(int nb)
{
	char	c;

	c = nb + '0';
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	unsigned int	new;
	int				mod;

	if (nb < 0)
	{
		new = -nb;
		write(1, "-", 1);
	}
	else
		new = nb;
	mod = new % 10;
	if (new >= 10)
	{
		new /= 10;
		ft_putnbr(new);
	}
	ft_putint(mod);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}
